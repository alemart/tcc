function Printer() { }

Printer.prototype.print = function(text) {
    var s = $("#printer").text();
    $("#printer").text(((s != "") ? (s + "\n") : s) + ("" + text));
}

Printer.prototype.text = function() {
    return $("#printer").text();
}
